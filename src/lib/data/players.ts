function makeHistory(basePoints: number, trend: TrendDirection): RankPoint[] {
  const months = [
    "Apr",
    "May",
    "Jun",
    "Jul",
    "Aug",
    "Sep",
    "Oct",
    "Nov",
    "Dec",
    "Jan",
    "Feb",
    "Mar",
  ];
  let pts = basePoints - (trend === "up" ? 800 : trend === "down" ? -200 : 400);
  return months.map((month, i) => {
    const delta = trend === "up" ? 80 : trend === "down" ? -20 : 30;
    const noise = Math.sin(i * 1.3) * 150;
    pts = Math.max(500, Math.min(9500, pts + delta + noise));
    return { month, points: Math.round(pts), rank: 0 };
  });
}

